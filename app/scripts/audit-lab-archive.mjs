import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const appRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const publicRoot = path.join(appRoot, "public");
const labRoot = path.join(publicRoot, "examples", "lab");

function walk(directory) {
  return fs.readdirSync(directory, { withFileTypes: true }).flatMap((entry) => {
    const fullPath = path.join(directory, entry.name);
    return entry.isDirectory() ? walk(fullPath) : [fullPath];
  });
}

function existsAsRoute(target) {
  if (fs.existsSync(target) && fs.statSync(target).isFile()) return true;
  return fs.existsSync(path.join(target, "index.html"));
}

function localTarget(reference, sourceFile) {
  const clean = reference.split("#", 1)[0].split("?", 1)[0];
  if (!clean || /^(?:https?:|mailto:|tel:|data:|javascript:|#)/i.test(clean)) {
    return null;
  }
  // Main application routes are generated by Vite rather than represented by
  // files inside public/. This archive audit owns Lab routes and static assets.
  if (clean === "/" || clean.startsWith("/tech-audit/")) return null;
  if (clean.startsWith("/")) return path.join(publicRoot, clean);
  return path.resolve(path.dirname(sourceFile), clean);
}

const failures = [];
const files = walk(labRoot);
const htmlFiles = files.filter((file) => file.endsWith(".html"));
const textFiles = files.filter((file) => /\.(?:css|html|js|json|txt|xml)$/i.test(file));

if (files.length < 166) failures.push(`expected at least 166 hosted files, found ${files.length}`);
if (htmlFiles.length < 47) failures.push(`expected at least 47 HTML pages, found ${htmlFiles.length}`);

const hubPath = path.join(labRoot, "index.html");
const hub = fs.readFileSync(hubPath, "utf8");
const manifestPath = path.join(labRoot, "concepts.json");
const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
const archive = JSON.parse(fs.readFileSync(path.join(labRoot, "archive.json"), "utf8"));
const conceptLinks = [...hub.matchAll(/class="concept-row[^"]*"[^>]*href="([^"]+)"/g)].map(
  (match) => match[1],
);
const suiteLinks = [...hub.matchAll(/class="lab-suite-card[^"]*"[^>]*href="([^"]+)"/g)].map(
  (match) => match[1],
);

if (conceptLinks.length !== 11) {
  failures.push(`expected 11 concept links, found ${conceptLinks.length}`);
}
if (suiteLinks.length !== 4) {
  failures.push(`expected 4 capability-suite links, found ${suiteLinks.length}`);
}

for (const reference of [...conceptLinks, ...suiteLinks]) {
  const target = localTarget(reference, hubPath);
  if (!target || !existsAsRoute(target)) failures.push(`missing Lab route: ${reference}`);
}

if (manifest.version !== 2) failures.push(`expected concepts manifest version 2, found ${manifest.version}`);
if (manifest.suites?.length !== 4) {
  failures.push(`expected 4 suites in concepts.json, found ${manifest.suites?.length ?? 0}`);
}
if (manifest.concepts?.length !== 11) {
  failures.push(`expected 11 concepts in concepts.json, found ${manifest.concepts?.length ?? 0}`);
}
if (archive.liveFiles !== files.length) {
  failures.push(`archive.json records ${archive.liveFiles} live files, found ${files.length}`);
}
if (archive.capabilitySuites !== manifest.suites?.length) {
  failures.push(
    `archive.json records ${archive.capabilitySuites} suites, found ${manifest.suites?.length ?? 0}`,
  );
}
if (archive.enhancedConcepts !== manifest.concepts?.length) {
  failures.push(
    `archive.json records ${archive.enhancedConcepts} enhanced concepts, found ${manifest.concepts?.length ?? 0}`,
  );
}

const suiteSlugs = (manifest.suites || []).map((suite) => suite.slug);
const manifestSlugs = (manifest.concepts || []).map((concept) => concept.slug);
if (new Set(suiteSlugs).size !== suiteSlugs.length) {
  failures.push("concepts.json contains duplicate suite slugs");
}
if (new Set(manifestSlugs).size !== manifestSlugs.length) {
  failures.push("concepts.json contains duplicate concept slugs");
}

const manifestConcepts = new Map(
  (manifest.concepts || []).map((concept) => [concept.slug, concept]),
);
const assignedConcepts = [];
for (const suite of manifest.suites || []) {
  const suitePath = path.join(labRoot, "suites", suite.slug, "index.html");
  if (!fs.existsSync(suitePath)) {
    failures.push(`missing suite page: ${suite.slug}`);
    continue;
  }
  const suiteSource = fs.readFileSync(suitePath, "utf8");
  if (!suiteSource.includes(`data-suite="${suite.slug}"`)) {
    failures.push(`suite page has the wrong identity: ${suite.slug}`);
  }
  for (const slug of suite.concepts || []) {
    assignedConcepts.push(slug);
    const concept = manifestConcepts.get(slug);
    if (!concept) failures.push(`${suite.slug} references unknown concept: ${slug}`);
    else if (concept.suite !== suite.slug) {
      failures.push(`${slug} is assigned to ${concept.suite}, not ${suite.slug}`);
    }
  }
}

for (const concept of manifest.concepts || []) {
  const conceptPath = path.join(labRoot, "concepts", concept.slug, "index.html");
  if (!fs.existsSync(conceptPath)) {
    failures.push(`missing concept page: ${concept.slug}`);
  } else {
    const conceptSource = fs.readFileSync(conceptPath, "utf8");
    if (!conceptSource.includes("/examples/lab/assets/lab-return.js")) {
      failures.push(`${concept.slug} is missing the shared Lab controls`);
    }
  }
  const assignmentCount = assignedConcepts.filter((slug) => slug === concept.slug).length;
  if (assignmentCount !== 1) {
    failures.push(`${concept.slug} is assigned to ${assignmentCount} suites`);
  }
}

const expectedConceptRoutes = new Set(
  manifestSlugs.map((slug) => `/examples/lab/concepts/${slug}/`),
);
const expectedSuiteRoutes = new Set(
  suiteSlugs.map((slug) => `/examples/lab/suites/${slug}/`),
);
for (const route of conceptLinks) {
  if (!expectedConceptRoutes.has(route)) failures.push(`unexpected concept link in hub: ${route}`);
}
for (const route of suiteLinks) {
  if (!expectedSuiteRoutes.has(route)) failures.push(`unexpected suite link in hub: ${route}`);
}
for (const route of expectedConceptRoutes) {
  if (!conceptLinks.includes(route)) failures.push(`hub is missing concept link: ${route}`);
}
for (const route of expectedSuiteRoutes) {
  if (!suiteLinks.includes(route)) failures.push(`hub is missing suite link: ${route}`);
}

for (const file of htmlFiles) {
  const source = fs.readFileSync(file, "utf8");
  const references = [...source.matchAll(/(?:href|src|poster)=["']([^"']+)["']/gi)].map(
    (match) => match[1],
  );
  for (const reference of references) {
    const target = localTarget(reference, file);
    if (target && !existsAsRoute(target)) {
      failures.push(`${path.relative(labRoot, file)} -> ${reference}`);
    }
  }
}

for (const file of files.filter((entry) => entry.endsWith(".css"))) {
  const source = fs.readFileSync(file, "utf8");
  const references = [...source.matchAll(/url\(\s*["']?([^"')]+)["']?\s*\)/gi)].map(
    (match) => match[1],
  );
  for (const reference of references) {
    const target = localTarget(reference, file);
    if (target && !existsAsRoute(target)) {
      failures.push(`${path.relative(labRoot, file)} -> ${reference}`);
    }
  }
}

const combinedText = textFiles.map((file) => fs.readFileSync(file, "utf8")).join("\n");
if (combinedText.includes("lab.littlefightnyc.com")) failures.push("old Lab hostname remains");
if (combinedText.includes("demo-colorway.netlify.app")) {
  failures.push("retired client-demo form destination remains");
}
if (/\bdata-netlify(?:=|\s)|\bnetlify-honeypot=/i.test(combinedText)) {
  failures.push("a preserved Lab demo can still register a live Netlify form");
}

const mainSiteText = [path.join(appRoot, "src"), publicRoot]
  .flatMap(walk)
  .filter((file) => /\.(?:css|html|js|json|tsx?|txt|xml)$/i.test(file))
  .map((file) => fs.readFileSync(file, "utf8"))
  .join("\n");
if (mainSiteText.includes("lab.littlefightnyc.com")) {
  failures.push("old Lab hostname remains in the main site");
}

const returnScript = fs.readFileSync(path.join(labRoot, "assets", "lab-return.js"), "utf8");
if (
  !returnScript.includes("'/examples/lab/suites/'") ||
  !returnScript.includes("lab-concept-shell__back")
) {
  failures.push("concept return chip does not point to its in-site capability suite");
}
for (const suiteSlug of suiteSlugs) {
  if (!returnScript.includes(`suite: '${suiteSlug}'`)) {
    failures.push(`concept return controls do not include suite: ${suiteSlug}`);
  }
}

const redirects = fs.readFileSync(path.join(publicRoot, "_redirects"), "utf8");
if (
  !redirects.includes("/labs/*") ||
  !redirects.includes("/examples/lab/:splat") ||
  !redirects.match(/^\/labs\s+\/examples\/lab\/\s+301$/m)
) {
  failures.push("the /labs shorthand redirects are incomplete");
}

const terminalBundle = fs.readFileSync(
  path.join(labRoot, "concepts", "terminal-3d", "assets", "index-DvF386rg.js"),
  "utf8",
);
if (!terminalBundle.includes("/examples/lab/concepts/terminal-3d/data/building.json")) {
  failures.push("Terminal 3D data path is not scoped to the archive");
}

const fieldGuide = fs.readFileSync(path.join(appRoot, "src", "pages", "FieldGuide.tsx"), "utf8");
if (
  !fieldGuide.includes('href="/examples/lab/" data-no-vt') ||
  fieldGuide.includes("lab.littlefightnyc.com")
) {
  failures.push("main Examples page is not linked to the in-site Lab hub");
}

if (failures.length > 0) {
  console.error(`Lab archive audit failed:\n- ${failures.join("\n- ")}`);
  process.exit(1);
}

console.log(
  `Lab archive audit OK — ${suiteLinks.length} suites, ${conceptLinks.length} concepts, ${htmlFiles.length} HTML pages, ${files.length} files.`,
);
